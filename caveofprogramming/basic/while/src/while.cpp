#include <iostream>
using namespace std;

int main()
{
	cout << "while" << endl; // prints while

	int i = 0;
	while (i < 5)
	{
		cout << i << " hello" << endl;
		i++; // increments i by 1
	}

	cout << "Programming quitting." << endl;

	return 0;
}
