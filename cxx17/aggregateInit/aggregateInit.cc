//------------------------------------------------------------
//C++03
//------------------------------------------------------------
struct S03
{
	 S03(const int t_i):i(t_i){}
	 int i;
};

//------------------------------------------------------------
//C++11
//------------------------------------------------------------

struct S11
{
	 int i;
	 float f;
};

//------------------------------------------------------------
//C++17
//------------------------------------------------------------

struct B
{
	 double q;
};

struct S17 : B
{
	 int i;
	 float f;
};

//------------------------------------------------------------
// MAIN
//------------------------------------------------------------

int main()
{
	// C++03
	// To initilize the struct we must have a constructor!
	 S03 s03(1);

	 // C++11 
	 // We use the uniform initialization syntax
	 S11 s11{1, 2.3f};

	 //C++17
	 //Base class initilization with default initialization
	 S17 s17{{}, 1, 2.3f};
	 // Base class member specific initialization
	 S17 s17a{{54.8}, 1, 2.3f};

	 return s03.i + s11.i + s17.i + s17a.i;
}
